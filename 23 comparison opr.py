#Comparison operator
a = 30
b = 40
c = 60

if a > b:
    print(" a is greater than b")

else:
    print("b is greater than a")

if a <= c:
    print("a is less than or equal to c")
else:
    print("a is greater than or equal to c")

if a != b:
    print("a is not equal to b")
else:
    print("a is equal to b")



