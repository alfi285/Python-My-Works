#Python program to find the maximum and minimum values in a numeric set?

# Create a numeric set
numeric_set = {12, 45, 7, 23, 56, 89, 34}

# Find maximum and minimum values
max_value = max(numeric_set)
min_value = min(numeric_set)

print("Numeric Set:", numeric_set)
print("Maximum Value:", max_value)
print("Minimum Value:", min_value)


